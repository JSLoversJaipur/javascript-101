// Traverse an Array
// Given an array, print or process each element in the array.

// Example:
// Question: Traverse and print the elements in the array [1, 2, 3, 4, 5].
// Explanation: We iterate through the array and perform an operation (in this case, print) on each element.
const elements = [1, 2, 3, 4, 5];

for (let i = 0; i < elements.length; i++) {
  console.log(elements[i]);
}