// Count the Number of Characters in a String
// Given a string, count the number of characters in the string.

// Example:1
// Question: How many characters are there in the string "Hello, world!"?
// Explanation: We can use the length property of a string to find the number of characters.
const string = "Hello, world!";
const characterCount = string.length;

console.log("The number of characters in the string is:", characterCount);

