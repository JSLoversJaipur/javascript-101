// Basic JavaScript Problem Solutions

// Problem 1: Calculate the Area of a Rectangle
// Write a JavaScript program to calculate and display the area of a rectangle given its length and width.

// Example:
// Question: Calculate the area of a rectangle with length 5 and width 3.
// Explanation: We can multiply the length and width to find the area of the rectangle.
const length = 5;
const width = 3;
const area = length * width;

console.log("The area of the rectangle is:", area);
