// The 'var' keyword has been available in JavaScript since its early days.
// It creates a variable with function or global scope.
var city = "Yuvraj";

// The 'let' keyword was introduced in ECMAScript 2015 (ES6).
// Variables defined with 'let' have block scope, meaning they are only accessible within the block they are defined in.
// Block scope refers to any code block surrounded by curly braces {} such as if statements, for loops, or functions.
let name = "Yuvraj";

// The 'const' keyword was also introduced in ECMAScript 2015 (ES6).
// Variables defined with 'const' are constants and their values cannot be changed once assigned.
// It's important to note that 'const' creates a read-only reference to a value, not an immutable value.
// This means that while the variable itself cannot be reassigned, complex objects or arrays assigned to it can still be modified.
const PI = 3.14;
